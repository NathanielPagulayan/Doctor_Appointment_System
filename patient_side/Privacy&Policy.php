        <br><p style="text-indent:20px; margin-left:30px; margin-right:30px; line-height:20px; font-weight:700;">
                C. DEFINITION OF TERMS
            </p>
                <p style="text-align:justify; text-indent: 50px; margin-left:30px; margin-right:30px; line-height:20px;">
                • Anonymization: refers to the processing of data to render it in such a way that the data subject is not or no longer identifiable.
                </p>
                <p style="text-align:justify; text-indent: 50px; margin-left:30px; margin-right:30px; line-height:20px;">
                • Anonymization: refers to the processing of data to render it in such a way that the data subject is not or no longer identifiable.
                </p>
                <p style="text-align:justify; text-indent: 50px; margin-left:30px; margin-right:30px; line-height:20px;">
                • Consent of the data subject: refers to any freely given, specific, informed indication of will, whereby the data subject agrees to the 
                collection and processing of personal information about and/or relating to him or her. It may be given on behalf of the data subject by an agent 
                specifically authorized by the data subject to do so.
                </p>
                <p style="text-align:justify; text-indent: 50px; margin-left:30px; margin-right:30px; line-height:20px;">
                •Data Subject: refers to an individual whose personal data is processed. This includes, among others, the clients, users and/or customers of DAS.
                </p>
                <p style="text-align:justify; text-indent: 50px; margin-left:30px; margin-right:30px; line-height:20px;">
                • Data Sharing: refers to the disclosure or transfer to a third party of personal data under the control or custody of a personal information 
                controller. The term excludes outsourcing, or the disclosure or transfer of personal data by a personal information controller to a personal 
                information processor.
                </p>
                <p style="text-align:justify; text-indent: 50px; margin-left:30px; margin-right:30px; line-height:20px;">
                • Processing: refers to any operation or any set of operations performed upon personal information including, but not limited to, the collection, 
                recording, organization, storage, updating or modification, retrieval, consultation, use, consolidation, blocking, erasure or destruction of data.
                </p>
                <p style="text-align:justify; text-indent: 50px; margin-left:30px; margin-right:30px; line-height:20px;">
                • Personal Information / Personal Data: refers to any information whether recorded in a material form or not, from which the identity of an 
                individual is apparent or can be reasonably and directly ascertained by the entity holding the information, or when put together with other 
                information would directly and certainly identify an individual.
                </p>
                <p style="text-align:justify; text-indent: 50px; margin-left:30px; margin-right:30px; line-height:20px;">
                • Personal Information Controller: refers to any person or organization who controls the collection, holding, processing or use of personal 
                information, including a person or organization who instructs another person or organization to collect, hold, process, use, transfer or disclose 
                personal information on his or her behalf.
                </p>
                <p style="text-align:justify; text-indent: 50px; margin-left:30px; margin-right:30px; line-height:20px;">
                • Personal Information Processor: refers to any qualified natural or juridical person to whom a personal information controller may outsource the 
                processing of personal data pertaining to a data subject.
                </p>
                <p style="text-align:justify; text-indent: 50px; margin-left:30px; margin-right:30px; line-height:20px;">
                • Sensitive Personal Information: refers to personal information (a) About an individual’s race, ethnic origin, marital status, age, color, 
                and religious, philosophical or political affiliations; (b) About an individual’s health, education, genetic or sexual life of a person, or to 
                any proceeding for any offense committed or alleged to have been committed by such person, the disposal of such proceedings, or the sentence of 
                any court in such proceedings; (c) Issued by government agencies peculiar to an individual which includes, but not limited to, social security 
                numbers, previous or current health records, licenses or its denials, suspension or revocation, and tax returns; and (d) Specifically established 
                by an executive order or an act of Congress to be kept classified.
                </p>

